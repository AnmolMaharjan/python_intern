x=5             # 1 0 1
y=6             # 1 1 0

# Bitwise AND operation
print(x & y)    # 1 0 0

# Bitwise OR operation
print(x | y)    # 1 1 1

# Bitwise NOT operation
print(~x)       # 1 0 1 0
print(~y)       # 1 0 0 1

# Bitwise XOR operation
print(x ^ y)    # 0 1 1

# Bitwise SHIFT LEFT operation
print(x<<2)     # 1 0 1 0 0

# Bitwise SHIFT RIGHT operation
print(x>>2)     # 1