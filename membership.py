
# %%
# - Membership operators are used to check if the element is present in the specified object or not.
# - basic membership operators:
#     - `in` (format: `a in x`)
#     - `not in` (format: `a not in x`)
# - membership operators return either `True` or `False`

x = [1,2,3,4,5]
print(5 in x)
print(6 in x)
print(5 not in x)
print(6 not in x)
